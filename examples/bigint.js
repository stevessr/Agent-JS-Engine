// BigInt Examples

// Basic BigInt literals
const small = 123n;
const large = 9007199254740991n; // MAX_SAFE_INTEGER + 1

// Different bases
const hexBigInt = 0xFFn;
const octalBigInt = 0o77n;
const binaryBigInt = 0b1111n;

// With numeric separators
const trillion = 1_000_000_000_000n;
const hexWithSep = 0xFF_FF_FFn;

// BigInt in expressions
const sum = 100n + 200n;
const product = 10n * 20n;

print("BigInt examples completed!");
